                                <h3>Cosa sono i Cookie</h3>
                                <p>
                                    I cookie sono stringhe di testo di piccole dimensioni che i siti visitati dall&apos;utente inviano al suo terminale
                                    (solitamente al browser), dove vengono memorizzati per essere poi ritrasmessi agli stessi siti alla successiva visita
                                    del medesimo utente. Nel corso della navigazione su un sito, l&apos;utente pu&ograve; ricevere sul suo terminale
                                    anche cookie che vengono inviati da siti o da web server diversi (c.d. &ldquo;terze parti&rdquo;), sui quali possono
                                    risiedere alcuni elementi (quali, ad esempio, immagini, mappe, suoni, specifici link a pagine di altri domini) presenti 
                                    sul sito che lo stesso sta visitando.
                                </p>

                                <h3>A cosa servono i Cookies</h3>
                                <p>
                                    I cookie possono essere suddivisi nelle seguenti categorie:
                                </p>
                                <ul>
                                    <li>
                                        <strong>Cookie (tecnici) di navigazione o di sessione</strong>
                                        Garantiscono la normale navigazione e fruizione del sito web (permettendo, ad esempio, di realizzare un 
                                        acquisto o autenticarsi per accedere ad aree riservate)
                                    </li>
                                    <li>
                                        <strong>Cookie (tecnici) analytics</strong>
                                        Assimilati ai cookie tecnici laddove utilizzati direttamente dal gestore del sito per raccogliere informazioni, 
                                        in forma aggregata, sul numero degli utenti e su come questi visitano il sito stesso
                                    </li>
                                    <li>
                                        <strong>Cookie (tecnici) di funzionalit&agrave;</strong>
                                        Permettono all&apos;utente la navigazione in funzione di una serie di criteri selezionati (ad esempio, la lingua,
                                        i prodotti selezionati per l&apos;acquisto) al fine di migliorare il servizio reso allo stesso
                                    </li>
                                    <li>
                                        <strong>Cookie di &ldquo;terze parti&rdquo;</strong>
                                        Installati sul terminale dell&apos;utente da parte di un sito diverso (c.d. "terze parti") che installa cookie per il
                                        tramite del primo sito che l&apos;utente sta visitando. Nel corso della navigazione su un sito, l&apos;utente 
                                        pu&ograve; ricevere sul suo terminale questi cookie che vengono inviati da siti o da web server diversi 
                                        (c.d. &ldquo;terze parti&rdquo;), sui quali possono risiedere alcuni elementi (quali, ad esempio, immagini,
                                        mappe, suoni, specifici link a pagine di altri domini) presenti sul sito che lo stesso sta visitando
                                    </li>
                                    <li>
                                        <strong>Cookie di profilazione</strong>
                                        Sono volti a creare profili relativi all&apos;utente e vengono utilizzati al fine di inviare messaggi pubblicitari in 
                                        linea con le preferenze manifestate dallo stesso nell&apos;ambito della navigazione in rete.
                                    </li>
                                </ul>

